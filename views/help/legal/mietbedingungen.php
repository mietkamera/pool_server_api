              ## 2. Gegenstand des Mietvertrags

Der Vermieter stellt dem Mieter Ausrüstung und Dienstleistungen zur Verfügung, die es dem Mieter ermöglichen, Einzelbilder in festgelegten Zeitabständen zu erzeugen, zu speichern und auf die gespeicherten Bilddaten über ein sicheres Internetportal zuzugreifen. Der Vermieter erzeugt außerdem täglich neue Zeitrafferfilme aus dem gespeicherten Bildmaterial für die gesamte Speicherdauer und die aktuelle Kalenderwoche.

#### 2.1 Mietsache

Der Vermieter stellt dem Mieter für die Dauer des Vertrages folgende technischen Komponenten zur Verfügung:

  - eine funktionstüchtige Kameraanlage, die folgende Komponenten beinhaltet:
    - ein Kamerasystem für die Bilderzeugung
    - ein Mobilfunkrouter inklusive Datenkarte mit ausreichend Datenvolumen
    - ein Befestigungssystem für die Montage an Masten

  - Speicherplatz für die Speicherung der Bilddaten

  - eine API (Application Programming Interface) zum Abruf der Daten mit Passwortschutz

#### 2.2 Nicht enthaltene Leistungen

Der Vermieter stellt folgende Leistungen nicht zur Verfügung:

  - keine Mastsysteme oder Arbeiten zur Errichtung von Mastsystemen

  - kein Aufbau / Montage durch den Vermieter

  - kein Rückbau / Demontage durch den Vermieter

#### 2.3 Voraussetzungen für den Betrieb

Für die Erbringung der Dienstleistung des Vermieters sind Voraussetzungen notwendig, die nicht vom Vermieter erbracht werden können. Der Mietvertrag kommt nur dann zustande, wenn diese Voraussetzungen dauerhaft erfüllt werden. Dazu gehören:

  - ein ausreichend gutes Mobilfunksignal vor Ort im LTE-Netz mit mindestens -70dBm Empfindlichkeit

  - ein 220 Volt Stromanschluss für die Kameraanlage 

  - eine Montagemöglichkeit an Mast, Kran, Wandkonsole, Gerüst oder einem Standfusssystem auf dem Flachdach.

Können diese Voraussetzungen vor Ort nicht erbracht werden, schuldet der Vermieter keine Leistung. Das gilt auch für den temporären Ausfall dieser Voraussetzungen.


## 3. Dauer des Mietvertrages

Der Mietvertrag gilt für mindestens einen Monat und beginnt - falls nicht anders vereinbart - mit dem Tag der Auslieferung.

#### 3.1 Verlängerung des Mietvertrages

Der Mietvertrag verlängert sich stillschweigend um jeweils einen Monat, wenn er nicht 
vorab gekündigt wird. 

#### 3.2 Kündigungsfristen

Es gelten keine Kündingsfristen. Der Mietvertrag kann jederzeit zum Ablauf des Mietmonats beendet werden.

#### 3.3 Sonderkündigung bei Vertragsbeginn

Der Mieter hat das Recht zur Kündigung des Mietvertrages vor Beginn der Aufnahmen ohne Angabe von Gründen. Der Vermieter räumt dem Mieter ausdrücklich diese Möglichkeit ein, um unvorhersehbare 
Umstände, die den Betrieb der Kameraanlage verhindern zu kompensieren.

#### 3.4 Sonderkündigung durch den Vermieter bei Vertragsbeginn

Sollte der Mieter nicht in der Lage sein, die unter Punkt 2.3 genannten Voraussetzungen für den Betrieb der Anlage zu erfüllen, schuldet der Vermieter keine Leistung und kann den Mietvertrag kündigen. 


## 4. Monatliche Mietkosten und einmalige Kosten

Die monatlichen Mietkosten ergeben sich aus dem Umfang der Nutzung der Kameraanlage, der Nutzung
des Speicherplatzes auf unseren Servern und der voraussichtlichen Länge der Mietdauer. 

#### 4.1 Einrichtungs- und Versandkosten

Der Mieter trägt die einmaligen Kosten für die Einrichtung des Speicherbereichs auf den Servern des Vermieters und den Versand der Anlage. Diese Kosten belaufen sich auf 60€ netto. Diese Kosten sind auch bei Wahrnehmung des Sonderkündigungsrechts durch den Mieter zu tragen. 

#### 4.2 Nebenkosten beim Betrieb der Kameraanlage

Die beim Betrieb der Kameraanlage verbrauchten Stromkosten trägt der Mieter.

#### 4.3 Kosten der Rücklieferung

Die Kosten der Rücklieferung übernimmt der Mieter. 


## 5. Datenschutz

#### 5.1 Verantwortliche Partei

Beim Betrieb der Kameraanlage durch den Mieter müssen alle datenschutzrechtlichen Erfordernisse durch den Mieter erfüllt werden. 

#### 5.2 Minimale Erfordernisse, die der Mieter erfüllen muss

    1. Eventuell Betroffenen muss nach Artikel 13 DSGVO der Zweck der Datenverarbeitung und die Frist der Speicherung personenbezogener Daten mitgeteilt werden. 

    2. Eine Datenschutzfolgenabschätzung nach Artikel 35 DSGVO muss durch den Mieter durchgeführt werden.

#### 5.3 Unterstützung durch den Vermieter

    1. Der Vermieter bietet dem Mieter gemäß Artikel 28DSGVO einen Vertrag zur Auftragsdatenverarbeiung an.

    2. Der Vermieter stellt im Verzeichnis der Verarbeitungstätigkeiten, die technischen und organisatorischen Schutzmaßnahmen dar. (zum Beispiel Passwortschutz und/oder statische Verpixelung von Bildbereichen).

#### 5.4 Keine Ansprüche Dritter

Der Vermieter übernimmt keinerlei Ansprüche, die von Seiten Dritter aus datenschutzrechtlichen Gründen an den Mieter, beim Betrieb der Kameraanlage erhoben werden.


