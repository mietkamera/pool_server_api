              <h5><small>6.1</small></h5>
              <p>Der Auftraggeber stellt für die Dauer der Diensterbringung durch ROLIX einen geeigneten Kamerastandort zur Verfügung und ermöglicht dem Personal 
              von ROLIX den Zugang zu diesem Standort. Ein geeigneter Standort ist eine vor Vandalismus und Diebstahl sichere Plattform mit einer Standfläche von 
              mindestens 2x2m, an der das UMTS-Netz der VODAFONE AG und ein zuverlässiger 220V-Stromanschluss in maximal 10m Entfernung verfügbar ist. Von dieser 
              Ebene aus wird die Kamera an einem bis zu 3,80m hohen verwindungssteifen Kameraturm befestigt.<br/>
              Alternativ kann die Kamera an einer Mastanlage montiert werden, die die Kamera und Übertragungstechnik mit einem Gewicht von ca 12Kg tragen kann. 
              Diese Mastanlage stellt der Auftraggeber. Er erbringt außerdem den Eignungsnachweis für ihre Tragfähigkeit.</p>
