  <h5>URL</h5>
  <code><span class="text-success">https://mobil.mietkamera.de/http-api/webcam/projekt/</span><abbr title="Shorttag der Kamera"><b><em>st</em></b></abbr>/<abbr title="Parameter der Methode"><b><em>par</em></b></abbr></code>
  <hr><h5>st = Shorttag</h5>
  <p>Der Shorttag ist ein sechsstelliger Verweis auf das Kameraverzeichnis. Er muss angegeben werden.</p>
  <h5>par = Parameter</h5>
  <p>Syntax: <code>?</code> Bisher sind keine Parameter definiert</p>
  <div class="row">
    <div class="col-sm-2 col-lg-1"><code>?</code></div>
    <div class="col-sm-8 col-lg-6"><p>Der Parameter ist noch nicht beschrieben</p></div>
  </div>
  
  <hr />
  <h5>Beispiel</h5>
  <code><b>https://mobil.mietkamera.de/http-api/webcam/projekt/</b>44a3f4/</code>
  <p>Der Aufruf liefert die Kameraseite f&uuml;r den Shorttag 44a3f4.</p>
